    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = -1;
        String code = jTextField1.getText();
        String name = jTextField2.getText();
        String quantity = jTextField3.getText();
        String price = jTextField4.getText();
        String category = (String)jComboBox1.getSelectedItem();
        try {
        String sql = "INSERT INTO Products(CODE, PRODUCT_NAME, QUANTITY, PRICE, CATEGORY) VALUES (?,?,?,?,?)";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, code);
        ps.setString(2, name);
        ps.setString(3, quantity);
        ps.setString(4, price);
        ps.setString(5, category);

        row = ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data insertion successful. Row:" + row, "Information", JOptionPane.INFORMATION_MESSAGE);
        
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        showAll();
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField3.setText(null);
        jTextField4.setText(null);
    }//GEN-LAST:event_jButton1ActionPerformed

