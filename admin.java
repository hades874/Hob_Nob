    private void categories()
    {
        try {
        //con=DriverManager.getConnection("jdbc:derby://localhost:1527/hobnob","user1","12345");
        String sql="SELECT * FROM CATEGORY";
        Statement st=con.createStatement();
        ResultSet rs = st.executeQuery(sql);
        while(rs.next())
        {
            String cats = rs.getString("CATEGORY_NAME");
            jComboBox1.addItem(cats);
            showAll();
        }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error in connection", JOptionPane.INFORMATION_MESSAGE);
        }
    }
